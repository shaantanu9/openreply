#!/usr/bin/env node
/**
 * Builds env.config.js for the static HTML site from dotenv files.
 * Browsers cannot read .env; this is the small bridge: .env + .env.example → env.config.js
 *
 * Usage (from anywhere):
 *   node act_suit/html_site/generate-env-config.mjs
 * Or:
 *   cd act_suit/html_site && node generate-env-config.mjs
 *
 * Merge order:
 *   1) .env.example
 *   2) .env (local, gitignored) — skipped if GAPMAP_ENV_SAMPLE_ONLY=1
 *   3) process.env for each PUBLIC_ENV_KEY (Vercel / CI) — wins over files
 *
 * On Vercel: set only the names in PUBLIC_ENV_KEYS in Project → Environment Variables
 * (available at build time). True secrets (service role, API keys) stay in server-only
 * env or Edge Functions; they are never read for env.config.js.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));

const EXAMPLE = path.join(__dirname, ".env.example");
const DOTENV = path.join(__dirname, ".env");
const OUT = path.join(__dirname, "env.config.js");

/**
 * Security boundary: ONLY these names are written to env.config.js (loaded in the browser).
 * Add nothing here that must stay secret. Server keys in .env are ignored unless listed here
 * (and they must never be listed here). Supabase *anon* key is a public client key; RLS protects data.
 */
const PUBLIC_ENV_KEYS = [
  "SUPABASE_URL",
  "SUPABASE_ANON_KEY",
  "LICENSE_API_BASE",
  "APP_DOWNLOAD_URL",
  "APP_DEEP_LINK_URL",
  "LEMONSQUEEZY_CHECKOUT_PRO",
  "LEMONSQUEEZY_CHECKOUT_LIVE_PASS",
  "LEMONSQUEEZY_CUSTOMER_PORTAL",
];

/** If present in merged .env, we log a one-line reminder — they are NOT copied to the frontend. */
const SERVER_ONLY_KNOWN = new Set([
  "SUPABASE_SERVICE_ROLE_KEY",
  "DEV_MINT_SECRET",
  "LEMONSQUEEZY_API_KEY",
  "LEMONSQUEEZY_WEBHOOK_SECRET",
  "STRIPE_SECRET_KEY",
  "STRIPE_WEBHOOK_SECRET",
  "OPENAI_API_KEY",
  "ANTHROPIC_API_KEY",
]);

function parseEnv(text) {
  const out = {};
  if (!text) return out;
  for (const line of text.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const m = trimmed.match(/^(?:export\s+)?([A-Za-z_][A-Za-z0-9_]*)\s*=\s*(.*)$/);
    if (!m) continue;
    const key = m[1];
    let val = m[2].trim();
    if (
      (val.startsWith('"') && val.endsWith('"')) ||
      (val.startsWith("'") && val.endsWith("'"))
    ) {
      val = val.slice(1, -1);
    }
    out[key] = val;
  }
  return out;
}

function readFileOptional(p) {
  try {
    return fs.readFileSync(p, "utf8");
  } catch {
    return "";
  }
}

const base = parseEnv(readFileOptional(EXAMPLE));
const sampleOnly =
  process.env.GAPMAP_ENV_SAMPLE_ONLY === "1" ||
  process.env.GAPMAP_ENV_SAMPLE_ONLY === "true";
const useLocal = !sampleOnly;
const local = useLocal ? parseEnv(readFileOptional(DOTENV)) : {};
const merged = { ...base, ...local };

const env = {};
for (const k of PUBLIC_ENV_KEYS) {
  const fromProcess = !sampleOnly ? process.env[k] : undefined;
  const fromFile = merged[k];
  if (fromProcess != null && String(fromProcess) !== "") {
    env[k] = String(fromProcess);
  } else if (fromFile != null && fromFile !== "") {
    env[k] = String(fromFile);
  } else {
    env[k] = "";
  }
}

for (const k of SERVER_ONLY_KNOWN) {
  if (merged[k] && String(merged[k]).trim() !== "") {
    console.warn(
      `generate-env-config: ${k} is in .env but is never written to env.config.js (server-only; stays off the frontend).`
    );
  }
  if (
    !sampleOnly &&
    process.env[k] &&
    String(process.env[k]).trim() !== ""
  ) {
    console.warn(
      `generate-env-config: ${k} is set in the environment but is not copied to env.config.js (by design; use only in server/Edge code).`
    );
  }
}

if (useLocal && !fs.existsSync(DOTENV)) {
  console.warn(
    "generate-env-config: no .env in act_suit/html_site/ (using .env.example + process.env). Copy .env.example to .env for local overrides."
  );
}
if (sampleOnly) {
  console.warn(
    "generate-env-config: GAPMAP_ENV_SAMPLE_ONLY=1 — not reading .env or deploy env; using .env.example only (for safe committed defaults / CI)."
  );
}

const body = `/**
 * AUTO-GENERATED by generate-env-config.mjs — do not edit by hand.
 * Source: .env + .env.example, overridden by process.env (e.g. Vercel) for PUBLIC keys. Re-run: npm run build
 * Public client config only (see PUBLIC_ENV_KEYS in generate-env-config.mjs). This file is loaded in the browser.
 */
window.GAPMAP_ENV = {
  SUPABASE_URL: ${JSON.stringify(env.SUPABASE_URL)},
  SUPABASE_ANON_KEY: ${JSON.stringify(env.SUPABASE_ANON_KEY)},
  LICENSE_API_BASE: ${JSON.stringify(env.LICENSE_API_BASE)},
  APP_DOWNLOAD_URL: ${JSON.stringify(env.APP_DOWNLOAD_URL)},
  APP_DEEP_LINK_URL: ${JSON.stringify(env.APP_DEEP_LINK_URL)},
  LEMONSQUEEZY_CHECKOUT_PRO: ${JSON.stringify(env.LEMONSQUEEZY_CHECKOUT_PRO)},
  LEMONSQUEEZY_CHECKOUT_LIVE_PASS: ${JSON.stringify(env.LEMONSQUEEZY_CHECKOUT_LIVE_PASS)},
  LEMONSQUEEZY_CUSTOMER_PORTAL: ${JSON.stringify(env.LEMONSQUEEZY_CUSTOMER_PORTAL)},
};
`;

fs.writeFileSync(OUT, body, "utf8");
console.log("Wrote", path.relative(process.cwd(), OUT));
