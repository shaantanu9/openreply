#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const EXAMPLE = path.join(__dirname, ".env.example");
const DOTENV = path.join(__dirname, ".env");
const OUT = path.join(__dirname, "env.config.js");

const PUBLIC_ENV_KEYS = [
  "SUPABASE_URL",
  "SUPABASE_ANON_KEY",
  "LICENSE_API_BASE",
  "APP_DOWNLOAD_URL",
  "APP_DEEP_LINK_URL",
  "LEMONSQUEEZY_CHECKOUT_PRO",
  "LEMONSQUEEZY_CHECKOUT_LIVE_PASS",
  "LEMONSQUEEZY_CUSTOMER_PORTAL",
];

function parseEnv(text) {
  const out = {};
  if (!text) return out;
  for (const line of text.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const m = trimmed.match(/^(?:export\s+)?([A-Za-z_][A-Za-z0-9_]*)\s*=\s*(.*)$/);
    if (!m) continue;
    let val = m[2].trim();
    if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
      val = val.slice(1, -1);
    }
    out[m[1]] = val;
  }
  return out;
}

function readFileOptional(p) {
  try { return fs.readFileSync(p, "utf8"); } catch { return ""; }
}

const base = parseEnv(readFileOptional(EXAMPLE));
const local = parseEnv(readFileOptional(DOTENV));
const merged = { ...base, ...local };

const env = {};
for (const k of PUBLIC_ENV_KEYS) {
  const fromProcess = process.env[k];
  const fromFile = merged[k];
  if (fromProcess != null && String(fromProcess) !== "") {
    env[k] = String(fromProcess);
  } else if (fromFile != null && fromFile !== "") {
    env[k] = String(fromFile);
  } else {
    env[k] = "";
  }
}

const body = `/**
 * AUTO-GENERATED by generate-env-config.mjs — do not edit by hand.
 * Public client config only. Source: .env + .env.example + process.env.
 */
window.OPENREPLY_ENV = {
  SUPABASE_URL: ${JSON.stringify(env.SUPABASE_URL)},
  SUPABASE_ANON_KEY: ${JSON.stringify(env.SUPABASE_ANON_KEY)},
  LICENSE_API_BASE: ${JSON.stringify(env.LICENSE_API_BASE)},
  APP_DOWNLOAD_URL: ${JSON.stringify(env.APP_DOWNLOAD_URL)},
  APP_DEEP_LINK_URL: ${JSON.stringify(env.APP_DEEP_LINK_URL)},
  LEMONSQUEEZY_CHECKOUT_PRO: ${JSON.stringify(env.LEMONSQUEEZY_CHECKOUT_PRO)},
  LEMONSQUEEZY_CHECKOUT_LIVE_PASS: ${JSON.stringify(env.LEMONSQUEEZY_CHECKOUT_LIVE_PASS)},
  LEMONSQUEEZY_CUSTOMER_PORTAL: ${JSON.stringify(env.LEMONSQUEEZY_CUSTOMER_PORTAL)},
};
`;

const DRY_RUN = process.argv.includes("--dry-run");
if (DRY_RUN) {
  console.log("Dry run — env.config.js would be:\n" + body);
} else {
  fs.writeFileSync(OUT, body, "utf8");
  console.log("Wrote", path.relative(process.cwd(), OUT));
}
